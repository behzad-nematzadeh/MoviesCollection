import 'package:moviescollection/src/domain/entities/movie.dart';

class MovieModel extends Movie {
  const MovieModel({
    required int id,
    required String title,
    required String poster,
    String? year,
    String? rated,
    String? released,
    String? runtime,
    String? director,
    String? writer,
    String? actors,
    String? plot,
    String? country,
    String? awards,
    String? metaScore,
    String? imdbRating,
    String? imdbVotes,
    String? imdbId,
    String? type,
    required List<dynamic> genres,
    required List<dynamic> images,
  }) : super(
            id: id,
            title: title,
            poster: poster,
            year: year,
            rated: rated,
            released: released,
            runtime: runtime,
            director: director,
            writer: writer,
            actors: actors,
            plot: plot,
            country: country,
            awards: awards,
            metaScore: metaScore,
            imdbRating: imdbRating,
            imdbVotes: imdbVotes,
            imdbId: imdbId,
            type: type,
            genres: genres,
            images: images);

  factory MovieModel.fromJson(Map<String, dynamic> json) {
    return MovieModel(
      id: json['id'] as int,
      title: json['title'] as String,
      poster: json['poster'] as String,
      year: json['year'] as String?,
      rated: json['rated'] as String?,
      released: json['released'] as String?,
      runtime: json['runtime'] as String?,
      director: json['director'] as String?,
      writer: json['writer'] as String?,
      actors: json['actors'] as String?,
      plot: json['plot'] as String?,
      country: json['country'] as String?,
      awards: json['awards'] as String?,
      metaScore: json['metascore'] as String?,
      imdbRating: json['imdb_rating'] as String?,
      imdbVotes: json['imdb_votes'] as String?,
      imdbId: json['imdb_id'] as String?,
      type: json['type'] as String?,
      genres: json['genres'] as List,
      images: json.containsKey('images') ? json['images'] as List : [],
    );
  }
}
